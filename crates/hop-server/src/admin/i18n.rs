pub fn l10n(locale: Locale) -> &'static L10n {
    match locale {
        Locale::En => &EN,
        Locale::Zh => &ZH,
    }
}

pub fn resolve_locale(headers: &HeaderMap) -> Locale {
    locale_from_cookie(headers)
        .or_else(|| locale_from_accept_language(headers))
        .unwrap_or(Locale::En)
}

pub fn locale_from_code(value: &str) -> Option<Locale> {
    match value.trim().to_ascii_lowercase().as_str() {
        "en" | "en-us" | "en-gb" => Some(Locale::En),
        "zh" | "zh-cn" | "zh-hans" | "zh-hans-cn" => Some(Locale::Zh),
        _ => None,
    }
}

fn locale_from_cookie(headers: &HeaderMap) -> Option<Locale> {
    let cookie = headers.get(header::COOKIE)?.to_str().ok()?;
    cookie.split(';').find_map(|part| {
        let (name, value) = part.trim().split_once('=')?;
        (name == LOCALE_COOKIE)
            .then(|| locale_from_code(value))
            .flatten()
    })
}

fn locale_from_accept_language(headers: &HeaderMap) -> Option<Locale> {
    let raw = headers.get(header::ACCEPT_LANGUAGE)?.to_str().ok()?;
    raw.split(',')
        .filter_map(|part| part.trim().split(';').next())
        .find_map(locale_from_code)
}

#[cfg(test)]
mod tests {
    use axum::http::{header, HeaderMap, HeaderValue};

    use super::*;

    #[test]
    fn locale_cookie_takes_priority_over_accept_language() {
        let mut headers = HeaderMap::new();
        headers.insert(
            header::COOKIE,
            HeaderValue::from_static("theme=dark; hop_lang=zh"),
        );
        headers.insert(
            header::ACCEPT_LANGUAGE,
            HeaderValue::from_static("en-US,en;q=0.9"),
        );

        assert_eq!(resolve_locale(&headers), Locale::Zh);
    }

    #[test]
    fn accept_language_detects_chinese_before_defaulting_to_english() {
        let mut headers = HeaderMap::new();
        headers.insert(
            header::ACCEPT_LANGUAGE,
            HeaderValue::from_static("zh-CN,zh;q=0.9,en;q=0.3"),
        );
        assert_eq!(resolve_locale(&headers), Locale::Zh);

        headers.insert(
            header::ACCEPT_LANGUAGE,
            HeaderValue::from_static("fr-FR,fr;q=0.9"),
        );
        assert_eq!(resolve_locale(&headers), Locale::En);
    }

    #[test]
    fn locale_instances_expose_translated_navigation() {
        assert_eq!(EN.nav_assets, "Assets");
        assert_eq!(ZH.nav_assets, "资产");
        assert_eq!(ZH.locale.code(), "zh-CN");
    }
}
